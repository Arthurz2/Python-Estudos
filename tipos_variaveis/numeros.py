# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)
# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type() - função type retorna o tipo da variavel que está sendo trabalhada
print("Tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel inteiro = ", type(numero_real))

#Operações aritiméticas
# soma +
soma = 1 + 1
print(soma)

# subtração
subtracao = 1 - 1
print(subtracao)

# multiplicação
multiplicacao = 2 * 2
print(multiplicacao)

# divisão
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo de variavel do resultiado da divisão =", type(divisao))

# Função int()
print("Valor em inteiro = ",int(divisao))

# Função float
print("Valor em float =", float(divisao))

# Modulo %
modulo = 5 % 2
print(modulo)

# divisao inteiro //
divisao = 5 // 2
print("Tipo de variavel do resultado da divisao =", type(divisao))